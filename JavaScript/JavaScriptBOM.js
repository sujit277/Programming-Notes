/* BOM --
BOM stands for Browser Object Model which allows JavaScript to talk
 to the Browser 

 Window object - 
 Window object represents browsers window. 
 Window object is supported by all the browsers.
 
 Window Size -
 Methods used to find the size of the browser window.
 1. window.innerHeight - The Inner height of the browser window.
 2. window.innerWidth - The Inner Width of the browser window.

 Window Screen --
 window.screen object contains information about the user's screen.

 Properties - 
 screen.width - returns width of the visitor's screen in pixels.
 screen.height - returns height of the visitor's screen in pixels.
 screen.availWidth - returns the width of the visitor's screen, in pixels, 
 minus interface features like the Windows Taskbar. 
 screen.availHeight - return height of the visitor's screen, in pixels
 minus interface features like the window taskbar.
 screen.colorDepth - returns the number of bits used to display one color.
 screen.pixelDepth - property returns the pixel depth of the screen.

 
 Window Location --
 window.location object can be used to get the current page address (URL) and 
 to redirect the browser to a new page.

window.location.href returns the href (URL) of the current page
window.location.hostname returns the domain name of the web host
window.location.pathname returns the path and filename of the current page
window.location.protocol returns the web protocol used (http: or https:)
window.location.assign() loads a new document


Window History --
Window.history object contains the browsers history.

It has following Methods 
history.back() - same as clicking back in the browser
history.forward() - same as clicking forward in the browser


Javascript has three types of popup boxes - Alert Box, Confirm box, Prompt box

Cookies--
Cookies lets us store user information in the webpage

 */